function generateSnapshots(): TrendSnapshot[] {
  const snapshots: TrendSnapshot[] = []
  const baseDate = new Date('2026-05-18')
  const dimBase = { schedule: 2, resource: 1, requirement: 1, dependency: 1 }
  for (let i = 0; i < 30; i++) {
    const d = new Date(baseDate)
    d.setDate(d.getDate() + i)
    const scheduleVar = Math.floor(Math.random() * 2)
    const resourceVar = Math.floor(Math.random() * 2)
    const reqVar = Math.floor(Math.random() * 3)
    const depVar = Math.floor(Math.random() * 2)
    const dim = {
      schedule: dimBase.schedule + scheduleVar + (i > 15 ? 1 : 0),
      resource: dimBase.resource + resourceVar + (i > 20 ? 1 : 0),
      requirement: dimBase.requirement + reqVar + (i > 10 ? 1 : 0),
      dependency: dimBase.dependency + depVar,
    }
    const total = dim.schedule + dim.resource + dim.requirement + dim.dependency
    const highRisks = Math.floor(total * 0.3) + (i > 15 ? 1 : 0)
    const exposureIndex = Math.round((6 + i * 0.3 + Math.random() * 2) * 10) / 10
    snapshots.push({
      id: `ts-${i}`,
      snapshotDate: d.toISOString().split('T')[0],
      totalRisks: total,
      highRisks,
      exposureIndex,
      dimensionBreakdown: dim,
    })
  }
  return snapshots
}

